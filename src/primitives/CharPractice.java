package primitives;

public class CharPractice {

    public static void main(String[] args) {

        char letter = 'T'; //  values of the char has to be in '' (quotation mark)

       // char letter2 = "A"; it will not compile

        System.out.println(letter); // T ---> ASCII table dec value for letter T is 84

        char letter3 = '9';

        System.out.println(letter3); // ---> ASCII table dec value for 9 is 57

        char symbol = '*';
        System.out.println(symbol);

        char space = ' ';
        System.out.println(space);

        int x =4;
        System.out.println(x);

        int something = 'A';
        System.out.println(something); // A is 65 in ASCII Table

        char something2 = 99;
        System.out.println(something2);

        char random = 356;
        System.out.println(random);

        char random2 = 102;
        System.out.println(random2);


    }
}
