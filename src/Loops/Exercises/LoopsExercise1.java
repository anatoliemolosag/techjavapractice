package Loops;

public class LoopsExercise1 {
    public static void main(String[] args) {
        for (int i = 1; i <=100 ; i++)
            if(i % 2 ==0)
                System.out.print(i + " ");

        for(int e = 100 ; e >=1 ; e--)
            if ( e % 2 ==0)
                System.out.print(e + " ");
    }
}
