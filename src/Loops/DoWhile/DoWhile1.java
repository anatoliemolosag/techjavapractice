package Loops;

public class DoWhile1 {
    public static void main(String[] args) {

        int a=3, b=5;
        do {
            System.out.println("This is Do While Loop");
            a++;
        } while(a<=b);

        a=3; b=5;
        do {
            System.out.println("This is Do While Loop $$$$$$$$");
            a++;
        } while(a>b);
}}
